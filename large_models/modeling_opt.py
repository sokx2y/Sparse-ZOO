def _loss_like_wrapper(self, shift_logits, input_ids, labels, option_len=None, num_options=None):
    # shift_labels 用 input_ids（对齐 forward_wrap_with_option_len）
    shift_labels = torch.clone(input_ids)[..., 1:].contiguous()

    pad_id = self.config.pad_token_id
    if pad_id is not None:
        shift_labels[shift_labels == pad_id] = -100

    # only_train_option：只算最后 option_len
    if option_len is not None:
        for _i, _len in enumerate(option_len):
            shift_labels[_i, :-_len] = -100

    loss_fct = CrossEntropyLoss(ignore_index=-100)

    if num_options is not None:
        log_probs = F.log_softmax(shift_logits, dim=-1)
        mask = shift_labels != -100

        shift_labels_safe = shift_labels.clone()
        shift_labels_safe[~mask] = 0

        selected_log_probs = torch.gather(
            log_probs, dim=-1, index=shift_labels_safe.unsqueeze(-1)
        ).squeeze(-1)

        selected_log_probs = (selected_log_probs * mask).sum(-1) / mask.sum(-1)

        if any([x != num_options[0] for x in num_options]):
            loss = 0
            start_id = 0
            count = 0
            while start_id < len(num_options):
                end_id = start_id + num_options[start_id]
                _logits = selected_log_probs[start_id:end_id].unsqueeze(0)
                _labels = labels[start_id:end_id][0].unsqueeze(0)
                loss = loss_fct(_logits, _labels) + loss
                count += 1
                start_id = end_id
            return loss / count
        else:
            nopt = num_options[0]
            logits_cls = selected_log_probs.view(-1, nopt)
            labels_cls = labels.view(-1, nopt)[:, 0]
            return loss_fct(logits_cls, labels_cls)

    return loss_fct(shift_logits.view(-1, self.config.vocab_size), shift_labels.view(-1))


