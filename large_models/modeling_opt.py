class OPTLearnedPositionalEmbedding(diffEmbedding):
    def __init__(
        self,
        num_embeddings: int,
        embedding_dim: int,
        layer_name: str = "",
        uv_provider=None,
        device=None,
        dtype=None,
    ):
        # OPT: position ids 需要 +2 的 offset hack
        self.offset = 2
        super().__init__(
            num_embeddings + self.offset,
            embedding_dim,
            layer_name=layer_name,
            uv_provider=uv_provider,
            device=device,
            dtype=dtype,
        )

    def forward(self, attention_mask: torch.LongTensor, past_key_values_length: int = 0):
        attention_mask = attention_mask.long()
        positions = (torch.cumsum(attention_mask, dim=1).type_as(attention_mask) * attention_mask).long() - 1
        positions = positions[:, past_key_values_length:]
        return super().forward(positions + self.offset)

    def forward_delta(self, attention_mask: torch.LongTensor, past_key_values_length: int = 0):
        attention_mask = attention_mask.long()
        positions = (torch.cumsum(attention_mask, dim=1).type_as(attention_mask) * attention_mask).long() - 1
        positions = positions[:, past_key_values_length:]
        # 把 positions+offset 当成 embedding 的 input ids，直接复用 diffEmbedding.forward_delta
        return super().forward_delta(positions + self.offset)


class OPTAttention(nn.Module):
    """Multi-headed attention from 'Attention Is All You Need' paper"""

    def __init__(
        self,
        embed_dim: int,
        num_heads: int,
        dropout: float = 0.0,
        is_decoder: bool = False,
        bias: bool = True,
    ):
        super().__init__()
        self.embed_dim = embed_dim
        self.num_heads = num_heads
        self.dropout = dropout
        self.head_dim = embed_dim // num_heads

        if (self.head_dim * num_heads) != self.embed_dim:
            raise ValueError(
                f"embed_dim must be divisible by num_heads (got `embed_dim`: {self.embed_dim}"
                f" and `num_heads`: {num_heads})."
            )
        self.scaling = self.head_dim**-0.5
        self.is_decoder = is_decoder

        self.k_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.v_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.q_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.out_proj = nn.Linear(embed_dim, embed_dim, bias=bias)

    def _shape(self, tensor: torch.Tensor, seq_len: int, bsz: int):
        return tensor.view(bsz, seq_len, self.num_heads, self.head_dim).transpose(1, 2).contiguous()

    def forward(
        self,
        hidden_states: torch.Tensor,
        key_value_states: Optional[torch.Tensor] = None,
        past_key_value: Optional[Tuple[torch.Tensor]] = None,
        attention_mask: Optional[torch.Tensor] = None,
        layer_head_mask: Optional[torch.Tensor] = None,
        output_attentions: bool = False,
    ) -> Tuple[torch.Tensor, Optional[torch.Tensor], Optional[Tuple[torch.Tensor]]]:
        """Input shape: Batch x Time x Channel"""

        # if key_value_states are provided this layer is used as a cross-attention layer
        # for the decoder
        is_cross_attention = key_value_states is not None

        bsz, tgt_len, _ = hidden_states.size()

        # get query proj
        query_states = self.q_proj(hidden_states) * self.scaling
        # get key, value proj
        if is_cross_attention and past_key_value is not None:
            # reuse k,v, cross_attentions
            key_states = past_key_value[0]
            value_states = past_key_value[1]
        elif is_cross_attention:
            # cross_attentions
            key_states = self._shape(self.k_proj(key_value_states), -1, bsz)
            value_states = self._shape(self.v_proj(key_value_states), -1, bsz)
        elif past_key_value is not None:
            # reuse k, v, self_attention
            key_states = self._shape(self.k_proj(hidden_states), -1, bsz)
            value_states = self._shape(self.v_proj(hidden_states), -1, bsz)
            key_states = torch.cat([past_key_value[0], key_states], dim=2)
            value_states = torch.cat([past_key_value[1], value_states], dim=2)
        else:
            # self_attention
            key_states = self._shape(self.k_proj(hidden_states), -1, bsz)
            value_states = self._shape(self.v_proj(hidden_states), -1, bsz)

        if self.is_decoder:
            # if cross_attention save Tuple(torch.Tensor, torch.Tensor) of all cross attention key/value_states.
            # Further calls to cross_attention layer can then reuse all cross-attention
            # key/value_states (first "if" case)
            # if uni-directional self-attention (decoder) save Tuple(torch.Tensor, torch.Tensor) of
            # all previous decoder key/value_states. Further calls to uni-directional self-attention
            # can concat previous decoder key/value_states to current projected key/value_states (third "elif" case)
            # if encoder bi-directional self-attention `past_key_value` is always `None`
            past_key_value = (key_states, value_states)

        proj_shape = (bsz * self.num_heads, -1, self.head_dim)
        query_states = self._shape(query_states, tgt_len, bsz).view(*proj_shape)
        key_states = key_states.view(*proj_shape)
        value_states = value_states.view(*proj_shape)

        src_len = key_states.size(1)
        attn_weights = torch.bmm(query_states, key_states.transpose(1, 2))

        if attn_weights.size() != (bsz * self.num_heads, tgt_len, src_len):
            raise ValueError(
                f"Attention weights should be of size {(bsz * self.num_heads, tgt_len, src_len)}, but is"
                f" {attn_weights.size()}"
            )

        if attention_mask is not None:
            if attention_mask.size() != (bsz, 1, tgt_len, src_len):
                raise ValueError(
                    f"Attention mask should be of size {(bsz, 1, tgt_len, src_len)}, but is {attention_mask.size()}"
                )
            attn_weights = attn_weights.view(bsz, self.num_heads, tgt_len, src_len) + attention_mask
            attn_weights = torch.max(attn_weights, torch.tensor(torch.finfo(attn_weights.dtype).min))
            attn_weights = attn_weights.view(bsz * self.num_heads, tgt_len, src_len)

        # upcast to fp32 if the weights are in fp16. Please see https://github.com/huggingface/transformers/pull/17437
        if attn_weights.dtype == torch.float16:
            attn_weights = nn.functional.softmax(attn_weights, dim=-1, dtype=torch.float32).to(torch.float16)
        else:
            attn_weights = nn.functional.softmax(attn_weights, dim=-1)

        if layer_head_mask is not None:
            if layer_head_mask.size() != (self.num_heads,):
                raise ValueError(
                    f"Head mask for a single layer should be of size {(self.num_heads,)}, but is"
                    f" {layer_head_mask.size()}"
                )
            attn_weights = layer_head_mask.view(1, -1, 1, 1) * attn_weights.view(bsz, self.num_heads, tgt_len, src_len)
            attn_weights = attn_weights.view(bsz * self.num_heads, tgt_len, src_len)

        if output_attentions:
            # this operation is a bit awkward, but it's required to
            # make sure that attn_weights keeps its gradient.
            # In order to do so, attn_weights have to be reshaped
            # twice and have to be reused in the following
            attn_weights_reshaped = attn_weights.view(bsz, self.num_heads, tgt_len, src_len)
            attn_weights = attn_weights_reshaped.view(bsz * self.num_heads, tgt_len, src_len)
        else:
            attn_weights_reshaped = None

        attn_probs = nn.functional.dropout(attn_weights, p=self.dropout, training=self.training)

        attn_output = torch.bmm(attn_probs, value_states)

        if attn_output.size() != (bsz * self.num_heads, tgt_len, self.head_dim):
            raise ValueError(
                f"`attn_output` should be of size {(bsz, self.num_heads, tgt_len, self.head_dim)}, but is"
                f" {attn_output.size()}"
            )

        attn_output = attn_output.view(bsz, self.num_heads, tgt_len, self.head_dim)
        attn_output = attn_output.transpose(1, 2)

        # Use the `embed_dim` from the config (stored in the class) rather than `hidden_state` because `attn_output` can be
        # partitioned aross GPUs when using tensor-parallelism.
        attn_output = attn_output.reshape(bsz, tgt_len, self.embed_dim)

        attn_output = self.out_proj(attn_output)

        return attn_output, attn_weights_reshaped, past_key_value
    
    def forward_delta(
        self,
        hidden_states: torch.Tensor,
        diff_hidden_states: torch.Tensor,
        key_value_states: Optional[torch.Tensor] = None,
        diff_key_value_states: Optional[torch.Tensor] = None,
        past_key_value: Optional[Tuple[torch.Tensor]] = None,
        attention_mask: Optional[torch.Tensor] = None,
        layer_head_mask: Optional[torch.Tensor] = None,
        output_attentions: bool = False,
    ):
        is_cross_attention = key_value_states is not None
        bsz, tgt_len, _ = hidden_states.size()

        if diff_hidden_states is None:
            diff_hidden_states = torch.zeros_like(hidden_states)
        if is_cross_attention and diff_key_value_states is None:
            diff_key_value_states = torch.zeros_like(key_value_states)
            
        # get query proj
        query_states, diff_query_states = self.q_proj.forward_delta(hidden_states, diff_hidden_states)
        query_states = query_states * self.scaling
        diff_query_states = diff_query_states * self.scaling

        def _unpack_past(pkv):
            if pkv is None:
                return None, None, None, None
            # 标准: (k, v)
            if len(pkv) == 2 and isinstance(pkv[0], torch.Tensor):
                return pkv[0], pkv[1], None, None
            # 扩展: ((k, dk), (v, dv))
            if len(pkv) == 2 and isinstance(pkv[0], (tuple, list)) and len(pkv[0]) == 2:
                (k, dk), (v, dv) = pkv
                return k, v, dk, dv
            # 扩展: (k, v, dk, dv)
            if len(pkv) == 4:
                return pkv[0], pkv[1], pkv[2], pkv[3]
            # 兜底
            return pkv[0], pkv[1], None, None

        past_k, past_v, past_dk, past_dv = _unpack_past(past_key_value)
        # get key, value proj
        if is_cross_attention and past_key_value is not None:
            # reuse cached cross-attn k/v
            key_states = past_k
            value_states = past_v
            diff_key_states = past_dk if past_dk is not None else torch.zeros_like(key_states)
            diff_value_states = past_dv if past_dv is not None else torch.zeros_like(value_states)
        elif is_cross_attention:
            # cross-attn: K,V 来自 key_value_states
            k, dk = self.k_proj.forward_delta(key_value_states, diff_key_value_states)
            v, dv = self.v_proj.forward_delta(key_value_states, diff_key_value_states)
            key_states = self._shape(k, -1, bsz)
            value_states = self._shape(v, -1, bsz)
            diff_key_states = self._shape(dk, -1, bsz)
            diff_value_states = self._shape(dv, -1, bsz)
        elif past_key_value is not None:
            # self-attn with cache: concat past + current
            k, dk = self.k_proj.forward_delta(hidden_states, diff_hidden_states)
            v, dv = self.v_proj.forward_delta(hidden_states, diff_hidden_states)

            key_states_cur = self._shape(k, -1, bsz)
            value_states_cur = self._shape(v, -1, bsz)
            diff_key_states_cur = self._shape(dk, -1, bsz)
            diff_value_states_cur = self._shape(dv, -1, bsz)

            key_states = torch.cat([past_k, key_states_cur], dim=2)
            value_states = torch.cat([past_v, value_states_cur], dim=2)

            past_dk = past_dk if past_dk is not None else torch.zeros_like(past_k)
            past_dv = past_dv if past_dv is not None else torch.zeros_like(past_v)
            diff_key_states = torch.cat([past_dk, diff_key_states_cur], dim=2)
            diff_value_states = torch.cat([past_dv, diff_value_states_cur], dim=2)
        else:
            # plain self-attn
            k, dk = self.k_proj.forward_delta(hidden_states, diff_hidden_states)
            v, dv = self.v_proj.forward_delta(hidden_states, diff_hidden_states)
            key_states = self._shape(k, -1, bsz)
            value_states = self._shape(v, -1, bsz)
            diff_key_states = self._shape(dk, -1, bsz)
            diff_value_states = self._shape(dv, -1, bsz)

        if self.is_decoder:
            # if cross_attention save Tuple(torch.Tensor, torch.Tensor) of all cross attention key/value_states.
            # Further calls to cross_attention layer can then reuse all cross-attention
            # key/value_states (first "if" case)
            # if uni-directional self-attention (decoder) save Tuple(torch.Tensor, torch.Tensor) of
            # all previous decoder key/value_states. Further calls to uni-directional self-attention
            # can concat previous decoder key/value_states to current projected key/value_states (third "elif" case)
            # if encoder bi-directional self-attention `past_key_value` is always `None`
            past_key_value = (key_states, value_states)
            # 如果你后续自己支持带 diff 的缓存，可改成：
            # past_key_value = ((key_states, diff_key_states), (value_states, diff_value_states))

        proj_shape = (bsz * self.num_heads, -1, self.head_dim)
        q = self._shape(query_states, tgt_len, bsz).view(*proj_shape)
        dq = self._shape(diff_query_states, tgt_len, bsz).view(*proj_shape)
        k = key_states.view(*proj_shape)
        dk = diff_key_states.view(*proj_shape)
        v = value_states.view(*proj_shape)
        dv = diff_value_states.view(*proj_shape)

        src_len = k.size(1)

        attn_scores = torch.bmm(q, k.transpose(1, 2))
        attn_scores_1 = torch.bmm(q + dq, (k + dk).transpose(1, 2))

        if attn_scores.size() != (bsz * self.num_heads, tgt_len, src_len):
            raise ValueError(
                f"Attention weights should be of size {(bsz * self.num_heads, tgt_len, src_len)}, but is {attn_scores.size()}"
            )

        if attention_mask is not None:
            if attention_mask.size() != (bsz, 1, tgt_len, src_len):
                raise ValueError(
                    f"Attention mask should be of size {(bsz, 1, tgt_len, src_len)}, but is {attention_mask.size()}"
                )

            attn_scores = attn_scores.view(bsz, self.num_heads, tgt_len, src_len) + attention_mask
            attn_scores_1 = attn_scores_1.view(bsz, self.num_heads, tgt_len, src_len) + attention_mask

            min_val = torch.tensor(torch.finfo(attn_scores.dtype).min, device=attn_scores.device, dtype=attn_scores.dtype)
            attn_scores = torch.max(attn_scores, min_val)
            attn_scores_1 = torch.max(attn_scores_1, min_val)

            attn_scores = attn_scores.view(bsz * self.num_heads, tgt_len, src_len)
            attn_scores_1 = attn_scores_1.view(bsz * self.num_heads, tgt_len, src_len)

        # upcast to fp32 if the weights are in fp16. Please see https://github.com/huggingface/transformers/pull/17437
        if attn_scores.dtype == torch.float16:
            attn_weights = nn.functional.softmax(attn_scores, dim=-1, dtype=torch.float32).to(torch.float16)
            attn_weights_1 = nn.functional.softmax(attn_scores_1, dim=-1, dtype=torch.float32).to(torch.float16)
        else:
            attn_weights = nn.functional.softmax(attn_scores, dim=-1)
            attn_weights_1 = nn.functional.softmax(attn_scores_1, dim=-1)

        if layer_head_mask is not None:
            if layer_head_mask.size() != (self.num_heads,):
                raise ValueError(
                    f"Head mask for a single layer should be of size {(self.num_heads,)}, but is {layer_head_mask.size()}"
                )
            mh = layer_head_mask.view(1, -1, 1, 1)
            attn_weights = (mh * attn_weights.view(bsz, self.num_heads, tgt_len, src_len)).view(bsz * self.num_heads, tgt_len, src_len)
            attn_weights_1 = (mh * attn_weights_1.view(bsz, self.num_heads, tgt_len, src_len)).view(bsz * self.num_heads, tgt_len, src_len)

        # output_attentions：只返回 base 的 reshape（和 forward 一致
        if output_attentions:
            attn_weights_reshaped = attn_weights.view(bsz, self.num_heads, tgt_len, src_len)
            attn_weights = attn_weights_reshaped.view(bsz * self.num_heads, tgt_len, src_len)
        else:
            attn_weights_reshaped = None

        attn_probs = F.dropout(attn_weights, p=self.dropout, training=self.training)
        attn_probs_1 = F.dropout(attn_weights_1, p=self.dropout, training=self.training)

        attn_output = torch.bmm(attn_probs, v)
        attn_output_1 = torch.bmm(attn_probs_1, v + dv)
        diff_attn_output = attn_output_1 - attn_output

        if attn_output.size() != (bsz * self.num_heads, tgt_len, self.head_dim):
            raise ValueError(
                f"`attn_output` should be of size {(bsz, self.num_heads, tgt_len, self.head_dim)}, but is {attn_output.size()}"
            )

        attn_output = attn_output.view(bsz, self.num_heads, tgt_len, self.head_dim)
        attn_output = attn_output.transpose(1, 2).reshape(bsz, tgt_len, self.embed_dim)

        attn_output_1 = attn_output_1.view(bsz, self.num_heads, tgt_len, self.head_dim)
        attn_output_1 = attn_output_1.transpose(1, 2).reshape(bsz, tgt_len, self.embed_dim)

        diff_attn_output = attn_output_1 - attn_output

        attn_output, diff_attn_output = self.out_proj.forward_delta(attn_output, diff_attn_output)

        return attn_output, diff_attn_output, attn_weights_reshaped, past_key_value


class OPTDecoderLayer(nn.Module):
    def __init__(self, config: OPTConfig):
        super().__init__()
        self.embed_dim = config.hidden_size
        self.self_attn = OPTAttention(
            embed_dim=self.embed_dim,
            num_heads=config.num_attention_heads,
            dropout=config.attention_dropout,
            is_decoder=True,
            bias=config.enable_bias,
        )
        self.do_layer_norm_before = config.do_layer_norm_before
        self.dropout = config.dropout
        self.activation_fn = ACT2FN[config.activation_function]

        self.self_attn_layer_norm = nn.LayerNorm(
            self.embed_dim, elementwise_affine=config.layer_norm_elementwise_affine
        )
        self.fc1 = nn.Linear(self.embed_dim, config.ffn_dim, bias=config.enable_bias)
        self.fc2 = nn.Linear(config.ffn_dim, self.embed_dim, bias=config.enable_bias)
        self.final_layer_norm = nn.LayerNorm(self.embed_dim, elementwise_affine=config.layer_norm_elementwise_affine)

    def forward(
        self,
        hidden_states: torch.Tensor,
        attention_mask: Optional[torch.Tensor] = None,
        layer_head_mask: Optional[torch.Tensor] = None,
        output_attentions: Optional[bool] = False,
        use_cache: Optional[bool] = False,
        past_key_value: Optional[Tuple[torch.Tensor]] = None,
    ) -> Tuple[torch.FloatTensor, Optional[Tuple[torch.FloatTensor, torch.FloatTensor]]]:
        """
        Args:
            hidden_states (`torch.FloatTensor`): input to the layer of shape `(batch, seq_len, embed_dim)`
            attention_mask (`torch.FloatTensor`, *optional*): attention mask of size
                `(batch, 1, tgt_len, src_len)` where padding elements are indicated by very large negative values.
            layer_head_mask (`torch.FloatTensor`, *optional*): mask for attention heads in a given layer of size
                `(encoder_attention_heads,)`.
            output_attentions (`bool`, *optional*):
                Whether or not to return the attentions tensors of all attention layers. See `attentions` under
                returned tensors for more detail.
            use_cache (`bool`, *optional*):
                If set to `True`, `past_key_values` key value states are returned and can be used to speed up decoding
                (see `past_key_values`).
            past_key_value (`Tuple(torch.FloatTensor)`, *optional*): cached past key and value projection states
        """

        residual = hidden_states

        # 125m, 1.7B, ..., 175B applies layer norm BEFORE attention
        if self.do_layer_norm_before:
            hidden_states = self.self_attn_layer_norm(hidden_states)

        # Self Attention
        hidden_states, self_attn_weights, present_key_value = self.self_attn(
            hidden_states=hidden_states,
            past_key_value=past_key_value,
            attention_mask=attention_mask,
            layer_head_mask=layer_head_mask,
            output_attentions=output_attentions,
        )
        hidden_states = nn.functional.dropout(hidden_states, p=self.dropout, training=self.training)
        hidden_states = residual + hidden_states

        # 350m applies layer norm AFTER attention
        if not self.do_layer_norm_before:
            hidden_states = self.self_attn_layer_norm(hidden_states)

        # Fully Connected
        hidden_states_shape = hidden_states.shape
        hidden_states = hidden_states.reshape(-1, hidden_states.size(-1))
        residual = hidden_states

        # 125m, 1.7B, ..., 175B applies layer norm BEFORE attention
        if self.do_layer_norm_before:
            hidden_states = self.final_layer_norm(hidden_states)

        hidden_states = self.fc1(hidden_states)
        hidden_states = self.activation_fn(hidden_states)

        hidden_states = self.fc2(hidden_states)
        hidden_states = nn.functional.dropout(hidden_states, p=self.dropout, training=self.training)

        hidden_states = (residual + hidden_states).view(hidden_states_shape)

        # 350m applies layer norm AFTER attention
        if not self.do_layer_norm_before:
            hidden_states = self.final_layer_norm(hidden_states)

        outputs = (hidden_states,)

        if output_attentions:
            outputs += (self_attn_weights,)

        if use_cache:
            outputs += (present_key_value,)

        return outputs
    
    def forward_delta(
        self,
        hidden_states: torch.Tensor,
        diff_hidden_states: torch.Tensor,
        attention_mask: Optional[torch.Tensor] = None,
        layer_head_mask: Optional[torch.Tensor] = None,
        output_attentions: Optional[bool] = False,
        use_cache: Optional[bool] = False,
        past_key_value: Optional[Tuple[torch.Tensor]] = None,
    ):

        residual = hidden_states
        diff_residual = diff_hidden_states

        # 125m, 1.7B, ..., 175B applies layer norm BEFORE attention
        if self.do_layer_norm_before:
            hidden_states, diff_hidden_states = self.self_attn_layer_norm.forward_delta(
                hidden_states, diff_hidden_states
            )

        # Self Attention
        # self.self_attn.forward_delta 返回: (attn_out, diff_attn_out, attn_weights_reshaped, present_kv)
        attn_out, diff_attn_out, self_attn_weights, present_key_value = self.self_attn.forward_delta(
            hidden_states=hidden_states,
            diff_hidden_states=diff_hidden_states,
            past_key_value=past_key_value,
            attention_mask=attention_mask,
            layer_head_mask=layer_head_mask,
            output_attentions=output_attentions,
        )
        attn_out_base = F.dropout(attn_out, p=self.dropout, training=self.training)
        attn_out_pert = F.dropout(attn_out + diff_attn_out, p=self.dropout, training=self.training)
        diff_attn_out_drop = attn_out_pert - attn_out_base
        hidden_states = residual + attn_out_base
        diff_hidden_states = diff_residual + diff_attn_out_drop

        # 350m applies layer norm AFTER attention
        if not self.do_layer_norm_before:
            hidden_states, diff_hidden_states = self.self_attn_layer_norm.forward_delta(
                hidden_states, diff_hidden_states
            )

        # Fully Connected 
        hidden_states_shape = hidden_states.shape
        hidden_states = hidden_states.reshape(-1, hidden_states.size(-1))
        diff_hidden_states = diff_hidden_states.reshape(-1, diff_hidden_states.size(-1))
        residual = hidden_states
        diff_residual = diff_hidden_states

        # 125m, 1.7B, ..., 175B applies layer norm BEFORE attention
        if self.do_layer_norm_before:
            hidden_states, diff_hidden_states = self.final_layer_norm.forward_delta(
                hidden_states, diff_hidden_states
            )

        # fc1
        hidden_states, diff_hidden_states = self.fc1.forward_delta(hidden_states, diff_hidden_states)
        # activation 
        act_base = self.activation_fn(hidden_states)
        act_pert = self.activation_fn(hidden_states + diff_hidden_states)
        hidden_states = act_base
        diff_hidden_states = act_pert - act_base
        # fc2
        hidden_states, diff_hidden_states = self.fc2.forward_delta(hidden_states, diff_hidden_states)
        # dropout
        ffn_out_base = F.dropout(hidden_states, p=self.dropout, training=self.training)
        ffn_out_pert = F.dropout(hidden_states + diff_hidden_states, p=self.dropout, training=self.training)
        diff_ffn_out_drop = ffn_out_pert - ffn_out_base

        # residual add + view back
        hidden_states = (residual + ffn_out_base).view(hidden_states_shape)
        diff_hidden_states = (diff_residual + diff_ffn_out_drop).view(hidden_states_shape)

        # 350m applies layer norm AFTER attention
        if not self.do_layer_norm_before:
            hidden_states, diff_hidden_states = self.final_layer_norm.forward_delta(
                hidden_states, diff_hidden_states
            )

        outputs = (hidden_states, diff_hidden_states)

        if output_attentions:
            outputs += (self_attn_weights,)

        if use_cache:
            outputs += (present_key_value,)

        return outputs


